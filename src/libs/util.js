util.formatDate = function (date, fmt) {
    if (/(y+)/.test(fmt)) {
        fmt = fmt.replace(RegExp.$1, (date.getFullYear() + '').substr(4 - RegExp.$1.length));
    }
    let o = {
        'M+': date.getMonth() + 1,
        'd+': date.getDate(),
        'h+': date.getHours(),
        'm+': date.getMinutes(),
        's+': date.getSeconds()
    };
    for (let k in o) {
        if (new RegExp(`(${k})`).test(fmt)) {
            let str = o[k] + '';
            fmt = fmt.replace(RegExp.$1, (RegExp.$1.length === 1) ? str : ('00' + str).substr(str.length));
        }
    }
    return fmt;
};

util.formatTime = function (time) {
    if (!time) {
        return '';
    } else {
        var date = new Date(time);
        return util.formatDate(date, 'yyyy-MM-dd hh:mm');
    }
};

util.findDicWord = function (vm, word) {
    let datas = vm.$store.state.app.dictionaryList;
    let result = datas.find((val) => {
        return val.dictCode === word;
    });
    return result.dictName;
};

